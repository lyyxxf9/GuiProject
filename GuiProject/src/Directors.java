    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:

        if(textT.getText().equals("")|| textG.getText().equals("") || textD.getText().equals("")){

            JOptionPane.showMessageDialog(this, "Don't Let it Blank, Please Enter Data!!");
        }else{

            String data[] = {textT.getText(),textG.getText(),textD.getText()};

            DefaultTableModel table= (DefaultTableModel)jTable1.getModel();

            table.addRow(data);
            JOptionPane.showMessageDialog(this, "Add Successfully!!!!!!");
            textT.setText("");
            textG.setText("");
            textD.setText("");

        }
    }//GEN-LAST:event_button3ActionPerformed

    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel table= (DefaultTableModel)jTable1.getModel();

        if(jTable1.getSelectedRowCount() == 1){
            table.removeRow(jTable1.getSelectedRow());

            JOptionPane.showMessageDialog(this, "Delete Successfully!!!");

        }else{
            if(jTable1.getRowCount()==0){

                JOptionPane.showMessageDialog(this, "Table is Empty");

            }else{

                JOptionPane.showMessageDialog(this, "Please Select For Single Row For Delete");
            }

        }
    }//GEN-LAST:event_button4ActionPerformed

    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel table= (DefaultTableModel)jTable1.getModel();

        if(jTable1.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Table is Empty");

        }else if(textT.getText().equals("")|| textG.getText().equals("") || textD.getText().equals("")){

            JOptionPane.showMessageDialog(this, "Don't Let it Blank, Please Enter Data!!");
        }

        else{

            if(jTable1.getSelectedRowCount()==1){

                
                String title= textT.getText();
                String genre= textG.getText();
                String direct= textD.getText();

                
                table.setValueAt(title,jTable1.getSelectedRow(), 0);
                table.setValueAt(genre,jTable1.getSelectedRow(), 1);
                table.setValueAt(direct,jTable1.getSelectedRow(), 2);

                JOptionPane.showMessageDialog(this, "Update successfully!");
               
                textT.setText("");
                textG.setText("");
                textD.setText("");

            }
        }
    }//GEN-LAST:event_button5ActionPerformed

